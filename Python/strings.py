name="Fazlıhan"
surname="Selvitopi"
age=16
me=("Hello. My name is "+name+" "+surname+". I am "+str(age)+" years old.")
print(me)                   #me yazdırdık
print(me[0])                #me 0'ıncı elemanını yazdırdık
print(me[8])                #me 8'inci elemanını yazdırdık
print(len(me))              #me karakter uzunluğunu bulduk
print(me[-1])               #me son karakterini yazdırdık
print(me[3:23])             #me 3-23 arasındali karakterleri yazdırdık
print(me[3:])               #me 3 ten sonraki karakterleri yazdırdık
print(me[:17])              #me son 17 karakter hariç yazdırdık
print(me[2:41:2])           #me 2-41 arası karakterleri (2 harf atlatarak) yazdırdık